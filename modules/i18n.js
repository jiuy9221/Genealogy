let _currentLang = "zh-CN";

function t(key) {
    return (TRANSLATIONS[_currentLang] || TRANSLATIONS["zh-CN"])[key]
        || TRANSLATIONS["zh-CN"][key]
        || key;
}

function getCurrentLang() { return _currentLang; }

function setLang(code) {
    if (!TRANSLATIONS[code]) return;
    _currentLang = code;
    localStorage.setItem(LANG_KEY, code);
    applyI18n();
    // Trigger a UI re-render if data is available
    if (window._onLangChange) window._onLangChange();
}

function loadLang() {
    const saved = localStorage.getItem(LANG_KEY);
    if (saved && TRANSLATIONS[saved]) _currentLang = saved;
}

function applyI18n() {
    document.querySelectorAll("[data-i18n]").forEach(el => {
        const key = el.dataset.i18n;
        const text = t(key);
        if (el.tagName === "INPUT") {
            el.placeholder = text;
        } else {
            el.textContent = text;
        }
    });
    document.querySelectorAll("[data-i18n-title]").forEach(el => {
        el.title = t(el.dataset.i18nTitle);
    });
    document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
        el.placeholder = t(el.dataset.i18nPlaceholder);
    });
    document.documentElement.lang = _currentLang === "en" ? "en" : "zh";
    // Sync lang select
    const sel = document.getElementById("lang-select");
    if (sel) sel.value = _currentLang;
}

window.t = t;
window.setLang = setLang;
window.getCurrentLang = getCurrentLang;
window.applyI18n = applyI18n;
window.loadLang = loadLang;
